from django.shortcuts import render, get_object_or_404, redirect
from .models import Student
from .forms import StudentForm
from .filters import StudentFilter

def student_list(request):
    student_filter = StudentFilter(request.GET, queryset=Student.objects.all())
    return render(request, 'grades/student_list.html', {'filter': student_filter})

def student_create(request):
    if request.method == 'POST':
        form = StudentForm(request.POST)
        if form.is_valid():
            form.save()
            return redirect('student_list')
    else:
        form = StudentForm()
    return render(request, 'grades/student_form.html', {'form': form})

def student_update(request, pk):
    student = get_object_or_404(Student, pk=pk)
    if request.method == 'POST':
        form = StudentForm(request.POST, instance=student)
        if form.is_valid():
            form.save()
            return redirect('student_list')
    else:
        form = StudentForm(instance=student)
    return render(request, 'grades/student_form.html', {'form': form})

def student_delete(request, pk):
    student = get_object_or_404(Student, pk=pk)
    if request.method == 'POST':
        student.delete()
        return redirect('student_list')
    return render(request, 'grades/student_confirm_delete.html', {'student': student})
